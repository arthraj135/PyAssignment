
# Taking user input for a number
user_input = input("Enter a number: ")
    
# Converting input to integer
#An integer in Python is a whole number, positive or negative, without any decimal point.
num_int = int(user_input)
# Converting input to complex
#A float in Python represents a floating-point number, which means it has a decimal point and can represent fractional values.
num_float = float(user_input)
# Converting input to complex
#A complex number in Python is represented as real_part + imaginary_part*j, where j is the imaginary unit.
num_complex = complex(user_input)
    # Printing the converted values
print("Integer:",num_int)
print("Float:",num_float)
print("Complex:" ,num_complex)

